"""Build Stage 1 hidden-state VQ artifacts for the selected R6c teacher.

The Joseph Stage 1 package used a windowed VQ-VAE over policy hidden states and
returned three public artifacts:

* train_codes.parquet
* code_summary.csv
* stage1_metrics.csv

This script builds the same handoff shape for the R6c K-window teacher.  It is
deliberately self-contained: it reads the Huawei result zips, replays the saved
policy, extracts policy hidden states, joins them to the rich R6c daily behavior
logs, and fits a windowed VQ-style KMeans codebook.  The K-window env calls the
policy once per macro-step and may emit several daily rows; the policy hidden
state is therefore broadcast to the daily rows generated by that macro-step.
"""

