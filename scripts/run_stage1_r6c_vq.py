def primary_rows_from_latest_fold(all_rows: pd.DataFrame, all_arrays: dict[str, np.ndarray]) -> tuple[pd.DataFrame, dict[str, np.ndarray]]:
    """Use fold_2021 train+validation as the main continuous 2010-2021 sequence."""

    mask = all_rows["fold"].eq("fold_2021") & all_rows["split"].isin(["train", "validation"])
    primary = all_rows.loc[mask].copy().reset_index(drop=True)
    idx = np.flatnonzero(mask.to_numpy())
    arrays = {key: value[idx] for key, value in all_arrays.items()}
    primary["date"] = pd.to_datetime(primary["date"]).dt.strftime("%Y-%m-%d")
    primary = primary.sort_values(["date", "split"]).drop_duplicates("date", keep="last").reset_index(drop=True)
    # Reorder arrays after date duplicate handling.
    kept_original = primary.index.to_numpy()
    # The sorted/drop operation reset the index, so recover by matching dates in
    # the original masked order.
    date_to_first_idx = {str(pd.Timestamp(date).date()): i for i, date in enumerate(all_rows.loc[idx, "date"])}
    ordered = np.asarray([date_to_first_idx[date] for date in primary["date"]], dtype=int)
    arrays = {key: value[ordered] for key, value in arrays.items()}
    return primary, arrays


def build_cross_fold_nmi(
    all_rows: pd.DataFrame,
    all_arrays: dict[str, np.ndarray],
    primary_codes: pd.DataFrame,
    *,
    k: int,
    window_length: int,
    seed: int,
) -> float:
    scores: list[float] = []
    primary_map = dict(zip(primary_codes["date"], primary_codes["code_id"]))
    for fold in sorted(all_rows["fold"].dropna().unique()):
        mask = all_rows["fold"].eq(fold) & all_rows["split"].isin(["train", "validation"])
        fold_rows = all_rows.loc[mask].copy().reset_index(drop=True)
        if len(fold_rows) < k + window_length:
            continue
        hidden = all_arrays["hidden"][np.flatnonzero(mask.to_numpy())]
        result = fit_kmeans_codes(hidden, k=k, window_length=window_length, seed=seed)
        dates = pd.to_datetime(fold_rows["date"]).dt.strftime("%Y-%m-%d").to_numpy()
        paired_a: list[int] = []
        paired_b: list[int] = []
        for date, code, valid in zip(dates, result["codes"], result["valid"]):
            if not valid or date not in primary_map:
                continue
            paired_a.append(int(primary_map[date]))
            paired_b.append(int(code))
        if len(set(paired_a)) > 1 and len(set(paired_b)) > 1:
            scores.append(float(normalized_mutual_info_score(paired_a, paired_b)))
    return float(np.mean(scores)) if scores else math.nan


