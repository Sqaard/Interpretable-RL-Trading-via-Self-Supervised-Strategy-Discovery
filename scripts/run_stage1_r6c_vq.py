@dataclass(frozen=True)
class ExtractedZip:
    zip_path: Path
    config: dict[str, Any]
    metadata: dict[str, Any]
    model_path: Path
    model_ready_path: Path
    fold_name: str


def read_zip_member(zf: zipfile.ZipFile, suffix: str) -> bytes:
    matches = [name for name in zf.namelist() if name.endswith(suffix)]
    if not matches:
        raise FileNotFoundError(f"No zip member ending with {suffix}")
    matches.sort(key=len)
    return zf.read(matches[0])


def read_zip_text(zf: zipfile.ZipFile, suffix: str) -> str:
    return read_zip_member(zf, suffix).decode("utf-8")


def extract_minimal_zip(zip_path: Path, tmp_dir: Path) -> ExtractedZip:
    with zipfile.ZipFile(zip_path) as zf:
        config = yaml.safe_load(read_zip_text(zf, "config.yaml"))
        metadata = json.loads(read_zip_text(zf, "/metadata.json"))
        model_bytes = read_zip_member(zf, "/model.zip")
        model_ready_bytes = read_zip_member(zf, "/model_ready.csv")

    fold = metadata["fold"]["fold"]
    fold_dir = tmp_dir / fold
    fold_dir.mkdir(parents=True, exist_ok=True)
    model_path = fold_dir / "model.zip"
    model_ready_path = fold_dir / "model_ready.csv"
    model_path.write_bytes(model_bytes)
    model_ready_path.write_bytes(model_ready_bytes)
    return ExtractedZip(
        zip_path=zip_path,
        config=config,
        metadata=metadata,
        model_path=model_path,
        model_ready_path=model_ready_path,
        fold_name=fold,
    )


def split_window(metadata: dict[str, Any], split: str) -> tuple[str, str]:
    fold = metadata["fold"]
    if split == "train":
        return fold["train_start"], fold["train_end_inclusive"]
    if split == "validation":
        return fold["validation_start"], fold["validation_end_inclusive"]
    raise ValueError(f"Unsupported split: {split}")


def flatten_array(prefix: str, values: np.ndarray, labels: list[str]) -> dict[str, float]:
    out: dict[str, float] = {}
    arr = np.asarray(values).reshape(-1)
    for i, value in enumerate(arr):
        suffix = labels[i] if i < len(labels) else str(i)
        out[f"{prefix}_{suffix}"] = float(value)
    return out


def replay_split(
    artifact: ExtractedZip,
    *,
    split: str,
    include_observations: bool,
) -> tuple[pd.DataFrame, dict[str, np.ndarray], dict[str, Any]]:
    """Replay a split and return one row per internal trading day."""

    start, end = split_window(artifact.metadata, split)
    panel = load_weight_panel(artifact.model_ready_path, start, end)
    env = make_env_from_config(panel, artifact.config, artifact.metadata["variant"])
    model = InstrumentedPPO.load(
        str(artifact.model_path),
        device="cpu",
        custom_objects={"instrumentation_dir": None},
    )
    model.policy.set_training_mode(False)

    obs, _ = env.reset()
    done = False
    macro_step = 0
    rows: list[dict[str, Any]] = []
    hidden_rows: list[np.ndarray] = []
    value_hidden_rows: list[np.ndarray] = []
    action_rows: list[np.ndarray] = []
    action_param_rows: list[np.ndarray] = []
    obs_rows: list[np.ndarray] = []
    log_prob_rows: list[float] = []
    entropy_rows: list[float] = []
    value_rows: list[float] = []

    while not done:
        layer_out = policy_layers(model.policy, obs)
        action = np.asarray(layer_out["action_mode"], dtype=np.float32)
        obs, reward, terminated, truncated, info = env.step(action)
        done = bool(terminated or truncated)

        daily_steps = info.get("daily_steps")
        if isinstance(daily_steps, list) and daily_steps:
            iterable = list(enumerate(daily_steps))
        else:
            iterable = [(0, info)]

        for daily_idx, daily_info in iterable:
            before = len(rows)
            append_eval_info_row(rows, panel, daily_info, float(daily_info.get("reward", reward)))
            row = rows[-1]
            row["fold"] = artifact.fold_name
            row["split"] = split
            row["source_zip"] = artifact.zip_path.name
            row["macro_step_id"] = macro_step
            row["daily_idx_in_macro_step"] = daily_idx
            row["macro_policy_update_date"] = str(info.get("date", daily_info.get("date", "")))
            if len(rows) != before + 1:
                raise RuntimeError("append_eval_info_row did not append exactly one row")

            hidden_rows.append(np.asarray(layer_out["policy_latent_64"], dtype=np.float32))
            value_hidden_rows.append(np.asarray(layer_out["value_latent"], dtype=np.float32))
            action_rows.append(np.asarray(layer_out["action_mode"], dtype=np.float32))
            action_param_rows.append(np.asarray(layer_out["raw_action_params"], dtype=np.float32))
            log_prob_rows.append(float(layer_out["log_prob_mode"]))
            entropy_rows.append(float(layer_out["entropy"]))
            value_rows.append(float(layer_out["value"]))
            if include_observations:
                obs_rows.append(np.asarray(layer_out["obs"], dtype=np.float32))

        macro_step += 1

    behavior = pd.DataFrame(rows)
    arrays: dict[str, np.ndarray] = {
        "hidden": np.vstack(hidden_rows).astype(np.float32),
        "value_latent": np.vstack(value_hidden_rows).astype(np.float32),
        "action_mode": np.vstack(action_rows).astype(np.float32),
        "raw_action_params": np.vstack(action_param_rows).astype(np.float32),
        "log_prob_mode": np.asarray(log_prob_rows, dtype=np.float32),
        "entropy": np.asarray(entropy_rows, dtype=np.float32),
        "value": np.asarray(value_rows, dtype=np.float32),
    }
    if include_observations:
        arrays["observations"] = np.vstack(obs_rows).astype(np.float32)

    meta = {
        "tickers": list(panel.tickers),
        "feature_columns": list(panel.feature_columns),
        "dates": list(panel.dates),
        "split_start": start,
        "split_end": end,
    }
    return behavior, arrays, meta


def zscore_fit(x: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    mean = x.mean(axis=0, keepdims=True)
    std = x.std(axis=0, keepdims=True)
    std = np.where(std < 1e-8, 1.0, std)
    return mean.astype(np.float32), std.astype(np.float32)


def centered_windows(x: np.ndarray, window: int) -> tuple[np.ndarray, np.ndarray]:
    if window % 2 != 1:
        raise ValueError("window_length must be odd")
    pad = window // 2
    n, d = x.shape
    padded = np.pad(x, ((pad, pad), (0, 0)), mode="edge")
    out = np.empty((n, window * d), dtype=np.float32)
    for i in range(n):
        out[i] = padded[i : i + window].reshape(-1)
    valid = np.ones(n, dtype=bool)
    if pad:
        valid[:pad] = False
        valid[-pad:] = False
    return out, valid


def fit_kmeans_codes(
    hidden: np.ndarray,
    *,
    k: int,
    window_length: int,
    seed: int,
) -> dict[str, Any]:
    mean, std = zscore_fit(hidden)
    z = (hidden - mean) / std
    windows, valid = centered_windows(z.astype(np.float32), window_length)
    train_x = windows[valid]
    model = KMeans(n_clusters=k, random_state=seed, n_init=20, max_iter=500)
    model.fit(train_x)
    codes = model.predict(windows).astype(np.int32)
    reconstructed = model.cluster_centers_[model.predict(train_x)]
    mse = float(np.mean((train_x - reconstructed) ** 2))
    var_h = float(np.var(train_x))
    counts = np.bincount(codes[valid], minlength=k).astype(float)
    probs = counts / max(counts.sum(), 1.0)
    nonzero = probs[probs > 0]
    entropy = -float(np.sum(nonzero * np.log(nonzero))) if nonzero.size else 0.0
    perplexity = float(np.exp(entropy))
    return {
        "model": model,
        "codes": codes,
        "valid": valid,
        "mean": mean,
        "std": std,
        "mse": mse,
        "var_h": var_h,
        "recon_over_var": mse / var_h if var_h > 0 else math.nan,
        "counts": counts,
        "utilization": float((counts > 0).sum() / k),
        "perplexity": perplexity,
    }


def apply_existing_codebook(
    hidden: np.ndarray,
    *,
    mean: np.ndarray,
    std: np.ndarray,
    model: KMeans,
    window_length: int,
) -> tuple[np.ndarray, np.ndarray]:
    z = ((hidden - mean) / std).astype(np.float32)
    windows, valid = centered_windows(z, window_length)
    return model.predict(windows).astype(np.int32), valid


def run_lengths(codes: np.ndarray, valid: np.ndarray | None = None) -> list[int]:
    if valid is not None:
        codes = codes[valid]
    if codes.size == 0:
        return []
    runs: list[int] = []
    current = int(codes[0])
    length = 1
    for value in codes[1:]:
        ivalue = int(value)
        if ivalue == current:
            length += 1
        else:
            runs.append(length)
            current = ivalue
            length = 1
    runs.append(length)
    return runs


def summarize_codes(
    frame: pd.DataFrame,
    *,
    code_col: str = "code_id",
    valid_col: str = "valid",
    k: int,
) -> pd.DataFrame:
    df = frame.loc[frame[valid_col]].copy()
    if "net_return" in df.columns:
        return_col = "net_return"
    elif "gross_return" in df.columns:
        return_col = "gross_return"
    else:
        return_col = None

    rows: list[dict[str, Any]] = []
    denom = max(len(df), 1)
    for code in range(k):
        sub = df.loc[df[code_col] == code]
        row = {
            "code_id": code,
            "n": int(len(sub)),
            "freq": float(len(sub) / denom),
            "mean_return": float(pd.to_numeric(sub[return_col], errors="coerce").mean()) if return_col and len(sub) else np.nan,
            "median_return": float(pd.to_numeric(sub[return_col], errors="coerce").median()) if return_col and len(sub) else np.nan,
            "mean_vix": float(pd.to_numeric(sub.get("market_feature_VIX", pd.Series(dtype=float)), errors="coerce").mean()) if len(sub) else np.nan,
            "mean_regime_p1": float(
                pd.to_numeric(sub.get("market_feature_Regime_1_Prob", pd.Series(dtype=float)), errors="coerce").mean()
            )
            if len(sub)
            else np.nan,
        }
        rows.append(row)
    return pd.DataFrame(rows)


def aggregate_behavior_by_code(frame: pd.DataFrame, *, k: int) -> pd.DataFrame:
    useful_patterns = (
        "q_",
        "cash_",
        "risk_stress",
        "recovery_score",
        "confidence_",
        "root_",
        "trade_",
        "turnover",
        "drawdown",
        "concentration",
        "topk_",
        "incremental_topk_",
        "group_",
        "market_feature_",
        "net_return",
        "gross_return",
        "reward",
    )
    numeric_cols = [
        col
        for col in frame.columns
        if col not in {"code_id", "date", "next_date", "fold", "split", "source_zip"}
        and any(col.startswith(pattern) or pattern in col for pattern in useful_patterns)
        and pd.api.types.is_numeric_dtype(frame[col])
    ]
    rows: list[dict[str, Any]] = []
    valid_frame = frame.loc[frame["valid"]].copy()
    for code in range(k):
        sub = valid_frame.loc[valid_frame["code_id"] == code]
        row: dict[str, Any] = {"code_id": code, "n": int(len(sub))}
        for col in numeric_cols:
            values = pd.to_numeric(sub[col], errors="coerce")
            if values.notna().any():
                row[f"mean_{col}"] = float(values.mean())
        rows.append(row)
    return pd.DataFrame(rows)


